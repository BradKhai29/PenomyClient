<script>
// Import dependencies section.
import { UrlHelper } from "src/helpers/UrlHelper";
import { useAuthStore } from "src/stores/common/AuthStore";
import { useUserProfileStore } from "src/stores/common/UserProfileStore";

// Import components section.
import HeaderHighlight from "src/components/common/creatorStudio/HeaderHighlight.vue";
import { UserProfileResponseDto } from "src/api.models/userProfile/userProfile1Page/UserProfileResponseDto";
import { UserProfile1ApiHandler } from "src/api.handlers/userProfile/userProfile1Page/UserProfile1ApiHandler";
import { NotificationHelper } from "src/helpers/NotificationHelper";

// Init store for later operation.
const authStore = useAuthStore();
const userProfileStore = useUserProfileStore();

export default {
    name: "UserProfileCard",
    components: {
        HeaderHighlight,
    },
    props: {
        userId: {
            type: String,
            required: true,
        },
    },
    data() {
        return {
            isLoading: true,
            inFriendList: false,
            showExtraInfo: false,
            isCopyLink: false,
            /**
             * @type {UserProfileResponseDto} The type of this data property.
             */
            loadedUserProfile: null,
        };
    },
    computed: {
        isAuth() {
            return authStore.isAuth;
        },
        /**
         * Check if the current user that visit this page is
         * the owner of this profile or not.
         */
        isProfileOwner() {
            return (
                // Check if the current user is already signed-in
                // and the userId is similar to the route param.
                authStore.isAuth &&
                this.userId == userProfileStore.currentUserId
            );
        },
        isCreator() {
            if (this.isProfileOwner) {
                return userProfileStore.isCreator;
            }

            return this.loadedUserProfile.isCreator;
        },
        tooltipOffset() {
            return [10, 10];
        },
        userProfileHref() {
            return window.location.href;
        },
        creatorProfileLink() {
            return `/creator/${this.userId}`;
        },
        nickname() {
            if (this.isProfileOwner) {
                return userProfileStore.userProfile.nickname;
            }

            return this.loadedUserProfile.nickname;
        },
        avatarUrl() {
            if (this.isProfileOwner) {
                return userProfileStore.currentAvatarUrl;
            }

            return this.loadedUserProfile.avatarUrl;
        },
        aboutMe() {
            if (this.isProfileOwner) {
                return userProfileStore.userProfile.aboutMe ?? "Không có mô tả";
            }

            return this.loadedUserProfile.aboutMe;
        },
        registeredAt() {
            if (this.isProfileOwner) {
                return userProfileStore.userProfile.registeredAt;
            }

            return this.loadedUserProfile.registeredAt;
        },
        lastActiveAt() {
            if (this.isProfileOwner) {
                return userProfileStore.userProfile.lastActiveAt;
            }

            return this.loadedUserProfile.lastActiveAt;
        },
        totalFollowedCreators() {
            if (this.isProfileOwner) {
                return userProfileStore.userProfile.totalFollowedCreators;
            }

            return this.loadedUserProfile.totalFollowedCreators;
        },
        // Creator profile section.
        becomeCreatorAt() {
            if (this.isProfileOwner) {
                return userProfileStore.userProfile.becomeCreatorAt;
            }

            return this.loadedUserProfile.becomeCreatorAt;
        },
        totalFollowers() {
            if (this.isProfileOwner) {
                return userProfileStore.userProfile.totalFollowers;
            }

            return this.loadedUserProfile.totalFollowers;
        },
        totalArtworks() {
            if (this.isProfileOwner) {
                return userProfileStore.userProfile.totalArtworks;
            }

            return this.loadedUserProfile.totalArtworks;
        },
    },
    async mounted() {
        // If the current user is the profile owner, then wait to load.
        if (this.isProfileOwner) {
            this.waitToLoadOwnerProfile();

            return;
        }

        // Otherwise, load the specified user by input userId.
        this.waitToLoadUserProfile();
    },
    methods: {
        copyProfileLink() {
            UrlHelper.copyCurrentUrlToClipBoard();

            this.isCopyLink = true;
        },
        /**
         * Wait for the api to load the current user profile
         * (also the owner of this profile).
         */
        async waitToLoadOwnerProfile() {
            await userProfileStore.waitToLoadOwnerProfile();

            this.isLoading = false;
        },
        /**
         * Wait for the api to load the user profile of specified user.
         */
        async waitToLoadUserProfile() {
            const result = await UserProfile1ApiHandler.getUserProfileAsync(
                authStore.accessToken,
                this.userId
            );

            // If the result is null, then redirect user
            // to homepage when the profile is not found.
            if (!result) {
                NotificationHelper.notifyError("Nguời dùng không tồn tại");

                this.$router.push("/");
                return;
            }

            // If not null, then display the result.
            this.loadedUserProfile = result;
            this.isLoading = false;
        },
    },
};
</script>

